export const getFacultyBySlug = (slug: string): Faculty | undefined =>
  FACULTIES.find((f) => f.slug === slug);

export const getTotalDepartments = (): number =>
  FACULTIES.reduce((sum, f) => sum + f.departments.length, 0);

export const getDepartmentById = (id: string) =>
  FACULTIES.flatMap((f) => f.departments).find((d) => d.id === id);
